package com.waoss.enesys.cpu.instructions;

import static com.waoss.enesys.cpu.instructions.InstructionConstants.instructions;

public enum InstructionName {
    /**
     * Add with carry
     */
    ADC,
    /**
     * Logical And
     */
    AND,
    ASL,
    BCC,
    BCS,
    BEQ,
    BIT,
    BMI,
    BNE,
    BPL,
    BRK,
    BVC,
    BVS,
    CLC,
    CLD,
    CLI,
    CLV,
    CMP,
    CPX,
    CPY,
    DEC,
    DEX,
    DEY,
    EOR,
    INC,
    INX,
    INY,
    JMP,
    JSR,
    LDA,
    LDX,
    LDY,
    LSR,
    NOP,
    ORA,
    PHA,
    PHP,
    PLA,
    PLP,
    ROL,
    ROR,
    RTI,
    RTS,
    SBC,
    SEC,
    SED,
    SEI,
    STA,
    STX,
    STY,
    TAX,
    TAY,
    TSX,
    TXA,
    TXS,
    TYA;

    public static InstructionName getByOpCode(int opCode) {
        return valueOf(instructions[opCode]);
    }

}
